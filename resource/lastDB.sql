-- Dumping structure for procedure gp.oas_SP_InLetterID_OutCirculation
DELIMITER //
CREATE PROCEDURE `oas_SP_InLetterID_OutCirculation`(
	IN `LetterID` INT
)
BEGIN
SELECT
CASE
WHEN main.LettersResponseID > 0 THEN main.LettersResponseID
ELSE main.LettersID
END AS id
,main.LettersNumber AS number,main.LettersSubject AS subject
,u_main.fullname AS sender,snd.SendLettersDate AS date_,u_snd.fullname AS receiver
,case snd.SendLettersReadType when 0 then '---' when 1 then 'خوانده شده' end AS read_type
,case main.LettersType when 0 then 'نامه' when 1 then 'پاسخ' end AS TYPE
,'ارسال/پاسخ' AS STATUS
FROM oas_letters main
INNER JOIN oas_sendletters snd ON main.LettersID = snd.LettersID_FK
INNER JOIN user u_main ON u_main.id = main.UsersID_FK
INNER JOIN user u_snd ON u_snd.id = snd.UsersID_FK
WHERE main.LettersDraftType = 1 AND main.LettersID = LetterID

UNION ALL

SELECT
CASE
WHEN main.LettersResponseID > 0 THEN main.LettersResponseID
ELSE main.LettersID
END AS id
,main.LettersNumber AS number,ref.ReferralLettersDescription AS subject
,u_ref_snd.fullname AS sender
,ref.ReferralLettersDate AS date_
,u_ref_rsv.fullname AS receiver
,case ref.ReferralLettersReadType when 0 then '---' when 1 then 'خوانده شده' end AS read_type
,case main.LettersType when 0 then 'نامه' when 1 then 'پاسخ' end AS TYPE
,'ارجاع' AS STATUS
FROM oas_letters main
INNER JOIN oas_referralletters ref ON main.LettersID = ref.LettersID_FK
INNER JOIN user u_main ON u_main.id = main.UsersID_FK
INNER JOIN user u_ref_snd ON u_ref_snd.id = ref.UsersID_Sender
INNER JOIN user u_ref_rsv ON u_ref_rsv.id = ref.UsersID_Receiver
WHERE main.LettersID = LetterID OR main.LettersResponseID = LetterID;
END//
DELIMITER ;

